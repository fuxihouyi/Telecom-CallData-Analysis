package com.abner.ct.web.bean;

public class AllCalllogAvg {

    private String month;
    private double avg_call;
    private double avg_dura;

    public String getMonth() {
        return month;
    }

    public void setMonth(String month) {
        this.month = month;
    }

    public double getAvg_call() {
        return avg_call;
    }

    public void setAvg_call(double avg_call) {
        this.avg_call = avg_call;
    }

    public double getAvg_dura() {
        return avg_dura;
    }

    public void setAvg_dura(double avg_dura) {
        this.avg_dura = avg_dura;
    }
}
